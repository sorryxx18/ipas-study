def validate_content(seg_id, content):
    if not content.startswith("# "):
        raise ValueError("id=%d: content must start with an H1 title" % seg_id)
    for heading in REQUIRED_HEADINGS:
        if heading not in content:
            raise ValueError("id=%d: missing required heading '%s'" % (seg_id, heading))
    if len(content) < 1800:
        raise ValueError(
            "id=%d: content is %d chars, expected >= 1800" % (seg_id, len(content))
        )
    if "❌" not in content or "✅" not in content:
        raise ValueError("id=%d: missing traps markers" % seg_id)


def main():
    if not GUIDE_PATH.exists():
        print("[ERROR] guide file not found:", GUIDE_PATH, file=sys.stderr)
        return 2
    if not STATE_PATH.exists():
        print("[ERROR] state file not found:", STATE_PATH, file=sys.stderr)
        return 2

    for seg_id in TARGET_IDS:
        validate_content(seg_id, CONTENTS[seg_id])

    with GUIDE_PATH.open("r", encoding="utf-8") as f:
        guide = json.load(f)

    if not isinstance(guide, dict) or "segments" not in guide:
        print("[ERROR] guide_s3.json shape unexpected — no 'segments' key", file=sys.stderr)
        return 3

    segments = guide["segments"]
    if not isinstance(segments, list):
        print("[ERROR] guide_s3.json 'segments' is not a list", file=sys.stderr)
        return 3

    id_to_index = {seg.get("id"): idx for idx, seg in enumerate(segments)}
    missing = [i for i in TARGET_IDS if i not in id_to_index]
    if missing:
        print("[ERROR] missing target ids in guide_s3.json:", missing, file=sys.stderr)
        return 3

    updated = []
    for seg_id in TARGET_IDS:
        idx = id_to_index[seg_id]
        seg = segments[idx]
        old_len = len(seg.get("content", ""))
        seg["content"] = CONTENTS[seg_id]
        new_len = len(seg["content"])
        updated.append(seg_id)
        print("[OK] id=%d title=%r content %d -> %d" % (seg_id, seg.get("title"), old_len, new_len))

    tmp_path = GUIDE_PATH.with_suffix(".json.tmp")
    with tmp_path.open("w", encoding="utf-8") as f:
        json.dump(guide, f, ensure_ascii=False, indent=2)
        f.write("\n")
    os.replace(tmp_path, GUIDE_PATH)
    print("[OK] wrote", GUIDE_PATH)

    with STATE_PATH.open("r", encoding="utf-8") as f:
        state = json.load(f)

    s3 = state.setdefault("s3", {})
    completed = s3.setdefault("completed", [])
    completed_set = set(completed)
    completed_set.update(TARGET_IDS)
    s3["completed"] = sorted(completed_set)
    now = datetime.utcnow()
    state["lastUpdated"] = now.strftime("%Y-%m-%dT%H:%M:%S.") + ("%03d" % (now.microsecond // 1000)) + "Z"

    tmp_state = STATE_PATH.with_suffix(".json.tmp")
    with tmp_state.open("w", encoding="utf-8") as f:
        json.dump(state, f, ensure_ascii=False, indent=2)
        f.write("\n")
    os.replace(tmp_state, STATE_PATH)
    print("[OK] wrote", STATE_PATH, "; s3.completed now includes", sorted(TARGET_IDS))

    print("[DONE] updated ids:", updated)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
